/**
 * Inject all theme CSS vars as a <style> tag into <head>.
 * Called once at app startup. Bypasses Tailwind's build-time purging entirely
 * since the CSS is generated from JS data at runtime.
 */
export function injectThemeStyles(): void {
  if (document.getElementById('saw-themes')) return // already injected

  const css = THEMES.map(t => {
    const selector = t.mode === 'dark' ? `.dark.theme-${t.id}` : `.theme-${t.id}`
    const vars = Object.entries(t.vars).map(([k, v]) => `  ${k}: ${v};`).join('\n')
    return `${selector} {\n${vars}\n}`
  }).join('\n\n')

  const style = document.createElement('style')
  style.id = 'saw-themes'
  style.textContent = css
  document.head.appendChild(style)
}
