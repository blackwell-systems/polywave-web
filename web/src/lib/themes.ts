// Single source of truth for all color themes.
// Adding a theme: append one object to THEMES — no other file needs editing.
//
// Vars are HSL channels (no hsl() wrapper): "H S% L%"
// Tailwind expects this format since colors are defined as hsl(var(--x)).
//
// mode: 'dark'  → selector .dark.theme-<id>  (activates when dark class + theme class both present)
// mode: 'light' → selector .theme-<id>       (activates in light mode when theme class present)

export interface ThemeDef {
  id: string
  label: string
  mode: 'light' | 'dark'
  vars: Record<string, string>
}

